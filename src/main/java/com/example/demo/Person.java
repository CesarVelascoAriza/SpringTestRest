package com.example.demo;

public class Person {
	
	
	private int id ;
	private String nombre;
	
	
	
	public Person() {
		this.id =1;
		this.nombre= "Cesar";
	}



	public int getId() {
		return id;
	}



	public void setId(int id) {
		this.id = id;
	}



	public String getNombre() {
		return nombre;
	}



	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

	
}
